dayOfWeek=int(input("number between 1 and 7:"))
if dayOfWeek==1:
    print("Monday")
elif(dayOfWeek==2):
    print("Tuesday")
elif(dayOfWeek==3):
    print("Wednesday")
elif(dayOfWeek==4):
    print("Thursday")
elif(dayOfWeek==5):
    print("Friday")
elif(dayOfWeek==6):
    print("Saturday")
elif(dayOfWeek==7):
    print("Sunday")


